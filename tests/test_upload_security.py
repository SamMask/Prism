def test_upload_exe_disguised_as_jpg(client, malicious_exe):
    """測試上傳偽裝成 JPG 的 EXE 檔案（應該被拒絕）"""
    data = {
        'file': (io.BytesIO(malicious_exe), 'malicious.jpg')
    }
    response = client.post('/api/upload', data=data, content_type='multipart/form-data')
    
    assert response.status_code == 400
    assert 'File content validation failed' in response.json['message']


